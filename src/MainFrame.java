    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        String nama = txtNama.getText();
        String telp = txtTelepon.getText();
    
    if(nama.isEmpty()|| telp.isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "Nama dan No Telp jangan kosong!");
        return;
    }

    try {
        java.sql.Connection c = Koneksi.getKoneksi(); 
        
        java.sql.PreparedStatement cek = c.prepareStatement("SELECT id FROM peserta WHERE nama = ? OR no_telp = ?");
        cek.setString(1, nama);
        cek.setString(2, telp);
        java.sql.ResultSet rs = cek.executeQuery();
        
        if (rs.next()) {
            // 2. Kalau ADA, maka UPDATE
            int idLama = rs.getInt("id");
            java.sql.PreparedStatement update = c.prepareStatement("UPDATE peserta SET nama = ?, no_telp = ? WHERE id = ?");
            update.setString(1, nama);
            update.setString(2, telp);
            update.setInt(3, idLama);
            update.executeUpdate();
            javax.swing.JOptionPane.showMessageDialog(this, "Data Peserta di-Update, Bos!");
        } else {
            // 3. Kalau TIDAK ADA, maka INSERT baru
            java.sql.PreparedStatement insert = c.prepareStatement("INSERT INTO peserta (nama, no_telp) VALUES (?, ?)");
            insert.setString(1, nama);
            insert.setString(2, telp);
            insert.executeUpdate();
            javax.swing.JOptionPane.showMessageDialog(this, "Peserta Baru Ditambahkan!");
        }
        
        tampilkanData();
        txtNama.setText("");
        txtTelepon.setText("");
    } catch (Exception e) {
        System.out.println("Gagal simpan: " + e.getMessage());
    }
    }//GEN-LAST:event_btnSimpanActionPerformed

