    private void tampilPopupGantiPassword() {
    javax.swing.JDialog gantiDialog = new javax.swing.JDialog(this, "Ubah Password", true);
    gantiDialog.setLayout(new java.awt.GridLayout(4, 2, 5, 5));

    javax.swing.JPasswordField txtLama = new javax.swing.JPasswordField();
    javax.swing.JPasswordField txtBaru = new javax.swing.JPasswordField();
    javax.swing.JPasswordField txtUlangi = new javax.swing.JPasswordField();
    javax.swing.JButton btnUbah = new javax.swing.JButton("Ubah");

    gantiDialog.add(new javax.swing.JLabel(" Password Lama:")); gantiDialog.add(txtLama);
    gantiDialog.add(new javax.swing.JLabel(" Password Baru:")); gantiDialog.add(txtBaru);
    gantiDialog.add(new javax.swing.JLabel(" Ulangi Password:")); gantiDialog.add(txtUlangi);
    gantiDialog.add(new javax.swing.JLabel("")); gantiDialog.add(btnUbah);

    btnUbah.addActionListener(e -> {
        String lama = new String(txtLama.getPassword());
        String baru = new String(txtBaru.getPassword());
        String ulang = new String(txtUlangi.getPassword());

        try {
            java.sql.Connection c = Koneksi.getKoneksi();
            // Cek password lama bener gak
            java.sql.ResultSet rs = c.createStatement().executeQuery("SELECT password FROM admin WHERE username='admin'");
            if(rs.next() && rs.getString("password").equals(lama)) {
                if(baru.equals(ulang)) {
                    java.sql.PreparedStatement p = c.prepareStatement("UPDATE admin SET password=? WHERE username='admin'");
                    p.setString(1, baru);
                    p.executeUpdate();
                    JOptionPane.showMessageDialog(gantiDialog, "Password Berhasil Diubah!");
                    gantiDialog.dispose();
                } else {
                    JOptionPane.showMessageDialog(gantiDialog, "Password baru nggak cocok, Bos!");
                }
            } else {
                JOptionPane.showMessageDialog(gantiDialog, "Password lama salah!");
            }
        } catch(Exception ex) { System.out.println(ex.getMessage()); }
    });

    gantiDialog.setSize(350, 200);
    gantiDialog.setLocationRelativeTo(this);
    gantiDialog.setVisible(true);
}
    
    private void btnKocokActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnKocokActionPerformed
        try {
        java.sql.Connection c = Koneksi.getKoneksi();
        java.sql.ResultSet rs = c.createStatement().executeQuery("SELECT nama FROM peserta WHERE status_menang = 0");
        java.util.ArrayList<String> listNama = new java.util.ArrayList<>();
        while(rs.next()) listNama.add(rs.getString("nama"));

        if(listNama.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Gak ada kandidat, Bos!");
            return;
        }

        // Munculin Roda
        javax.swing.JDialog dialog = new javax.swing.JDialog(this, "RODA ARISAN", true);
        PanelRoda roda = new PanelRoda(listNama);
        dialog.add(roda);
        dialog.pack();
        dialog.setLocationRelativeTo(this);

        // Timer Animasi Putar
        javax.swing.Timer timer = new javax.swing.Timer(20, null);
        final double[] speed = {20.0 + new java.util.Random().nextDouble() * 20};
        final double[] currentAngle = {0};

        timer.addActionListener(e -> {
            currentAngle[0] += speed[0];
            roda.setSudut(currentAngle[0]);
            speed[0] *= 0.97; // Efek gesekan (melambat)

            if (speed[0] < 0.5) {
                timer.stop();
                // Hitung siapa yang ditunjuk jarum (0 derajat di atas)
                double finalAngle = (360 - (currentAngle[0] % 360) + 90) % 360;
                int indexWinner = (int) (finalAngle / (360.0 / listNama.size())) % listNama.size();
                String pemenang = listNama.get(indexWinner);
                
                javax.swing.JOptionPane.showMessageDialog(dialog, "Selamat! " + pemenang + " Menang!");
                
                // Update DB
                try {
                    c.createStatement().executeUpdate("UPDATE peserta SET status_menang = 1 WHERE nama = '" + pemenang + "'");
                    tampilkanData();
                } catch(Exception ex) {}
                dialog.dispose();
            }
        });

        timer.start();
        dialog.setVisible(true);

    } catch (Exception e) {}
    }//GEN-LAST:event_btnKocokActionPerformed

