    private void tampilkanData() {
    DefaultTableModel model = new DefaultTableModel();
    model.addColumn("ID");
    model.addColumn("No");
    model.addColumn("Nama Peserta");
    model.addColumn("No Telp");
    String[] bulan = {"Jan", "Feb", "Mar", "Apr", "Mei", "Jun", "Jul", "Agu", "Sep", "Okt", "Nov", "Des"};
    for (String b : bulan) model.addColumn(b);
    model.addColumn("Total Setoran");
    model.addColumn("Status");
    
    int totalUangSekampung = 0;

    try {
        java.sql.Connection c = Koneksi.getKoneksi();
        java.sql.ResultSet rs = c.createStatement().executeQuery("SELECT * FROM peserta");
        int no = 1;

        while(rs.next()) {
            int totalPerOrang = 0;
            Object[] row = new Object[18]; // 18 kolom
            row[0] = rs.getInt("id");
            row[1] = no++;
            row[2] = rs.getString("nama");
            row[3] = rs.getString("no_telp");
            
            // Isi data b1 - b12 ke tabel
            for(int i = 1; i <= 12; i++) {
                int bayar = rs.getInt("b" + i);
                row[i + 3] = bayar; // Mulai dari index ke-3 di tabel
                totalPerOrang += bayar;
            }
            
            row[16] = "Rp " + totalPerOrang;
            row[17] = rs.getInt("status_menang") == 0 ? "Belum" : "MENANG ðŸŽ‰";
            totalUangSekampung += totalPerOrang;
            model.addRow(row);
        }
        tblPeserta.setModel(model);
        java.text.NumberFormat nf = java.text.NumberFormat.getInstance(new java.util.Locale("id", "ID"));
        lblGrandTotal.setText("Total Uang: Rp " + nf.format(totalUangSekampung));
        
        // Sembunyiin ID
        tblPeserta.getColumnModel().getColumn(0).setMinWidth(0);
        tblPeserta.getColumnModel().getColumn(0).setMaxWidth(0);
        
    } catch (Exception e) { System.out.println(e.getMessage()); }
}


